#include <stdio.h>

int main(void){

    //Creating two new lines after printing statement
    printf("Hello, World!\n\n");

    //Creating a horizontal tab in the printing statement
    printf("Hello, World!\tAnd the Internet!\n");

    //Creating a blackslash in the prinitng statement
    printf("Hello, World!\\And the Internet!\n");

    //Printing double quotes in the printing statement
    printf("Hello, \"World\"!");

    return 0;
}
