#include <stdio.h>
#include <math.h> //Must include math.h header file to use math functions

int main(void){

    printf("Calling sqrt():\n\n");
    printf("The square root of 16: %.2f\n\n", sqrt(16));

    printf("Calling pow():\n\n");
    printf("4 to the power of 3: %.2f\n\n", pow(4, 3));

    return 0;
}
